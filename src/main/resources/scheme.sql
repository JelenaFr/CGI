drop table if exists appointment  cascade;
drop table if exists dentist_names  cascade;


create table appointment (
                             id bigint generated by default as identity,
                             date binary(255),
                             is_available boolean,
                             period varchar(255),
                             dentist_id bigint,
                             primary key (id)
);

create table dentist_names (
                               id bigint generated by default as identity,
                               name varchar(255),
                               primary key (id)
);

alter table if exists appointment
    add constraint FK
        foreign key (dentist_id)
            references dentist_names;

